def area(a, h):
    '''
    a: сторона квадрата
    h: высота квадрата
    Возвращаемое значение: произведение a и h деленное на два
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    a: сторона квадрата
    b: сторона квадрата
    c: сторона квадрата
    Возвращаемое значение: сумма трех чисел a,b и с
    '''
    return a + b + c

